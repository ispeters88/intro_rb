# What do the following expressions evaluate to?

=begin
1. x = 2
  2
2. puts x = 2
  nil (return value of puts is always nil)
3. p name = "Joe"
  "Joe"
    => p returns the object passed to it (unlike puts which returns nil)
4. four = "four"
  "four"
5. print something = "nothing"
  nil (return value of print is always nil)
=end